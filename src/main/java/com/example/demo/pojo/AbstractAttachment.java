package com.example.demo.pojo;

public abstract class AbstractAttachment {

	private AttachmentObjectLink objectLink;

	/**
	 * @return the objectLink
	 */
	public AttachmentObjectLink getObjectLink() {
		return objectLink;
	}

	/**
	 * @param objectLink
	 *            the objectLink to set
	 */
	public void setObjectLink(AttachmentObjectLink objectLink) {
		this.objectLink = objectLink;
	}

}
